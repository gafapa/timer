function normalizeLanguage(value) {
  if (typeof value !== "string" || value.trim() === "") {
    return null;
  }

  const code = value.toLowerCase().split("-")[0];
  return supportedLanguages.has(code) ? code : null;
}

export function detectInitialLanguage() {
  if (typeof window === "undefined") {
    return DEFAULT_LANGUAGE;
  }

  const storedLanguage = normalizeLanguage(window.localStorage.getItem(LANGUAGE_STORAGE_KEY));
  if (storedLanguage) {
    return storedLanguage;
  }

  return normalizeLanguage(window.navigator.language) ?? DEFAULT_LANGUAGE;
}

export function translate(language, key, params = {}) {
  const dictionary = TRANSLATIONS[language] ?? TRANSLATIONS[DEFAULT_LANGUAGE];
  let message = dictionary[key] ?? TRANSLATIONS[DEFAULT_LANGUAGE][key] ?? key;

  Object.entries(params).forEach(([token, value]) => {
    message = message.replaceAll(`{${token}}`, String(value));
  });

  return message;
}
